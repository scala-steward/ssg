class MarkdownTable(
  val tableChars: CharSequence,
  var options:    TableFormatOptions
) {

  val header:    TableSection = new TableSection(TableSectionType.HEADER)
  val separator: TableSection = new TableSeparatorSection(TableSectionType.SEPARATOR)
  val body:      TableSection = new TableSection(TableSectionType.BODY)
  val caption:   TableSection = new TableCaptionSection(TableSectionType.CAPTION)

  private var isHeading:       Boolean      = true
  private var _isSeparator:    Boolean      = false
  var formatTableIndentPrefix: CharSequence = if (options == null) "" else options.formatTableIndentPrefix

  // used by finalization and conversion to text
  private var alignments:   Array[CellAlignment] = scala.compiletime.uninitialized
  private var columnWidths: Array[Int]           = scala.compiletime.uninitialized

  // generated by conversion to text
  private val trackedOffsets: ArrayList[TrackedOffset] = new ArrayList[TrackedOffset]()

  private val ALL_SECTIONS:     Array[TableSection] = Array(header, separator, body, caption)
  private val ALL_TABLE_ROWS:   Array[TableSection] = Array(header, separator, body)
  private val ALL_CONTENT_ROWS: Array[TableSection] = Array(header, body)
  private val ALL_HEADER_ROWS:  Array[TableSection] = Array(header)
  private val ALL_BODY_ROWS:    Array[TableSection] = Array(body)

  def this(tableChars: CharSequence, options: Nullable[DataHolder]) = {
    this(tableChars, new TableFormatOptions(options))
  }

  def getCaptionCell: TableCell =
    if (caption.rows.size() > 0 && caption.rows.get(0).cells.size() > 0) caption.rows.get(0).cells.get(0)
    else TableCaptionSection.NULL_CELL

  def setCaptionCell(captionCell: TableCell): Unit = {
    if (caption.rows.size() == 0) {
      caption.rows.add(caption.defaultRow())
    }
    caption.rows.get(0).cells.clear()
    caption.rows.get(0).cells.add(captionCell)
  }

  def getCaption: BasedSequence = getCaptionCell.text

  def setCaption(caption: CharSequence): Unit = {
    val captionCell = getCaptionCell
    setCaptionCell(
      captionCell.withText(
        if (captionCell.openMarker.isEmpty()) "[" else captionCell.openMarker,
        caption,
        if (captionCell.closeMarker.isEmpty()) "]" else captionCell.closeMarker
      )
    )
  }

  /*
   * Used by visitor during table creation
   */
  def setCaptionWithMarkers(
    tableCellNode: Node,
    captionOpen:   CharSequence,
    caption:       CharSequence,
    captionClose:  CharSequence
  ): Unit =
    setCaptionCell(
      new TableCell(
        Nullable(tableCellNode),
        captionOpen,
        if (options.formatTableCaptionSpaces == DiscretionaryText.AS_IS) caption else BasedSequence.of(caption).trim(),
        captionClose,
        1,
        1
      )
    )

  def getHeadingRowCount:     Int = header.rows.size()
  def getSeparatorRowCount:   Int = separator.rows.size()
  def getBodyRowCount:        Int = body.rows.size()
  def getCaptionRowCount:     Int = caption.rows.size()
  def getMaxHeadingColumns:   Int = header.getMaxColumns
  def getMaxSeparatorColumns: Int = separator.getMaxColumns
  def getMaxBodyColumns:      Int = body.getMaxColumns

  def getHaveCaption: Boolean =
    caption.rows.size() > 0 && caption.rows.get(0).cells.size() > 0 && caption.rows.get(0).cells.get(0).columnSpan != 0

  def getMinColumns: Int = {
    val headingColumns   = header.getMinColumns
    val separatorColumns = separator.getMinColumns
    val bodyColumns      = body.getMinColumns
    min(
      if (headingColumns == 0) Integer.MAX_VALUE else headingColumns,
      separatorColumns,
      if (bodyColumns == 0) Integer.MAX_VALUE else bodyColumns
    )
  }

  def getMaxColumns: Int = {
    val headingColumns   = header.getMaxColumns
    val separatorColumns = separator.getMaxColumns
    val bodyColumns      = body.getMaxColumns
    max(headingColumns, separatorColumns, bodyColumns)
  }

  def getMinColumnsWithoutColumns(withSeparator: Boolean, skipColumns: Int*): Int =
    aggregateTotalColumnsWithoutColumns(if (withSeparator) ALL_TABLE_ROWS else ALL_CONTENT_ROWS, MinAggregator, skipColumns.toArray)

  def getMaxColumnsWithoutColumns(withSeparator: Boolean, skipColumns: Int*): Int =
    aggregateTotalColumnsWithoutColumns(if (withSeparator) ALL_TABLE_ROWS else ALL_CONTENT_ROWS, MaxAggregator, skipColumns.toArray)

  def getMinColumnsWithoutRows(withSeparator: Boolean, skipRows: Int*): Int =
    aggregateTotalColumnsWithoutRows(if (withSeparator) ALL_TABLE_ROWS else ALL_CONTENT_ROWS, MinAggregator, skipRows.toArray)

  def getMaxColumnsWithoutRows(withSeparator: Boolean, skipRows: Int*): Int =
    aggregateTotalColumnsWithoutRows(if (withSeparator) ALL_TABLE_ROWS else ALL_CONTENT_ROWS, MaxAggregator, skipRows.toArray)

  def getTrackedOffsets: JList[TrackedOffset] = trackedOffsets

  private def findTrackedOffset(offset: Int): Nullable[TrackedOffset] = {
    var result: Nullable[TrackedOffset] = Nullable(null)
    var done = false
    val iter = trackedOffsets.iterator()
    while (iter.hasNext && !done) {
      val to = iter.next()
      if (to.offset == offset) { result = Nullable(to); done = true }
      else if (to.offset > offset) { done = true }
    }
    result
  }

  def getTrackedOffset(offset: Int): Nullable[TrackedOffset] = findTrackedOffset(offset)

  def getTrackedOffsetIndex(offset: Int): Int = {
    val to = findTrackedOffset(offset)
    if (to.isEmpty) offset else to.get.getIndex
  }

  def getTableStartOffset: Int = {
    // MdNav:diagnostic/4134, index out of range
    val rows = getAllRows
    if (!rows.isEmpty) {
      val row = rows.get(0)
      row.normalizeIfNeeded()
      if (row.cells.size() > 0) row.cells.get(0).getStartOffset(Nullable(null))
      else 0
    } else {
      0
    }
  }

  def getCellOffsetInfo(offset: Int): TableCellOffsetInfo = boundary {
    var r       = 0
    val allRows = getAllSectionRows
    val iter    = allRows.iterator()
    while (iter.hasNext) {
      val row = iter.next()
      row.normalizeIfNeeded()
      val lastCell      = row.cells.get(row.cells.size() - 1)
      val lastSegment   = lastCell.getLastSegment
      var lineEndOffset = lastSegment.baseEndOfLineAnyEOL()
      if (lineEndOffset == -1) lineEndOffset = lastSegment.endOffset

      if (offset <= lineEndOffset) {
        // it is on this line
        var i = 0
        var previousCell: Nullable[TableCell] = Nullable(null)
        val cellIter = row.cells.iterator()
        while (cellIter.hasNext) {
          val cell = cellIter.next()
          if (if (!cell.closeMarker.isEmpty()) offset < cell.closeMarker.endOffset else offset <= cell.text.endOffset) {
            if (offset >= cell.getInsideStartOffset(previousCell) && offset <= cell.getInsideEndOffset) {
              // in the cell area
              break(
                new TableCellOffsetInfo(
                  offset,
                  this,
                  getAllRowsSection(r),
                  Nullable(row),
                  Nullable(cell),
                  r,
                  i,
                  Nullable(Integer.valueOf(i)),
                  Nullable(Integer.valueOf(offset - cell.getInsideStartOffset(previousCell)))
                )
              )
            } else {
              // it the span area or before pipe of first cell
              break(
                new TableCellOffsetInfo(offset, this, getAllRowsSection(r), Nullable(row), Nullable(cell), r, i, Nullable(null), Nullable(null))
              )
            }
          }
          i += 1
          previousCell = Nullable(cell)
        }
        // after the last cell
        break(
          new TableCellOffsetInfo(offset, this, getAllRowsSection(r), Nullable(row), Nullable(lastCell), r, i, Nullable(null), Nullable(null))
        )
      }
      r += 1
    }

    val lastSection = getAllRowsSection(r - 1)
    new TableCellOffsetInfo(offset, this, lastSection, Nullable(null), Nullable(null), r, 0, Nullable(null), Nullable(null))
  }

  def addTrackedOffset(trackedOffset: TrackedOffset): Boolean = {
    val offset = trackedOffset.offset
    trackedOffsets.removeIf(it => it.offset == offset)
    trackedOffsets.add(trackedOffset)

    val info = getCellOffsetInfo(offset)
    if (info.getInsideColumn) {
      // real cell, we can add it to the cells contents
      info.tableRow.get.cells.set(
        info.column,
        info.tableCell.get.withTrackedOffset(
          offset - info.tableCell.get.getTextStartOffset(if (info.column == 0) Nullable(null) else Nullable(info.tableRow.get.cells.get(info.column - 1))),
          trackedOffset.isAfterSpaceEdit,
          trackedOffset.isAfterDelete
        )
      )
      true
    } else if (info.isBeforeCells) {
      info.tableRow.get.beforeOffset = offset
      true
    } else if (info.isInCellSpan) {
      info.tableRow.get.cells.set(info.column, info.tableCell.get.withSpanTrackedOffset(offset - info.tableCell.get.getInsideEndOffset))
      true
    } else if (info.isAfterCells) {
      info.tableRow.get.afterOffset = offset
      true
    } else {
      false
    }
  }

  /*
      Table Manipulation Helper API
   */
  def getAllRows:        JList[TableRow] = getAllSectionsRows(ALL_TABLE_ROWS*)
  def getAllContentRows: JList[TableRow] = getAllSectionsRows(ALL_CONTENT_ROWS*)
  def getAllSectionRows: JList[TableRow] = getAllSectionsRows(ALL_SECTIONS*)

  private def getAllSectionsRows(sections: TableSection*): JList[TableRow] = {
    val rows = new ArrayList[TableRow](header.rows.size() + body.rows.size())
    for (section <- sections)
      rows.addAll(section.rows)
    rows
  }

  def isAllRowsSeparator(index: Int): Boolean =
    index >= header.rows.size() && index < header.rows.size() + separator.rows.size()

  def getAllRowsSection(index: Int): TableSection = {
    var remaining = index
    var found: TableSection = null // NOTE: null at interop boundary
    val iter = ALL_SECTIONS.iterator
    while (iter.hasNext && found == null) {
      val section = iter.next()
      if (remaining < section.rows.size()) {
        found = section
      } else {
        remaining -= section.rows.size()
      }
    }
    found
  }

  def getAllRowsCount:         Int = header.rows.size() + separator.rows.size() + body.rows.size()
  def getAllContentRowsCount:  Int = header.rows.size() + body.rows.size()
  def getAllSectionsRowsCount: Int = header.rows.size() + separator.rows.size() + body.rows.size() + caption.rows.size()

  def forAllRows(manipulator:        TableRowManipulator):                                     Unit = forAllSectionsRows(0, Integer.MAX_VALUE, ALL_TABLE_ROWS, manipulator)
  def forAllRows(startIndex:         Int, manipulator: TableRowManipulator):                   Unit = forAllSectionsRows(startIndex, Integer.MAX_VALUE, ALL_TABLE_ROWS, manipulator)
  def forAllRows(startIndex:         Int, count:       Int, manipulator: TableRowManipulator): Unit = forAllSectionsRows(startIndex, count, ALL_TABLE_ROWS, manipulator)
  def forAllContentRows(manipulator: TableRowManipulator):                                     Unit = forAllSectionsRows(0, Integer.MAX_VALUE, ALL_CONTENT_ROWS, manipulator)
  def forAllContentRows(startIndex:  Int, manipulator: TableRowManipulator):                   Unit = forAllSectionsRows(startIndex, Integer.MAX_VALUE, ALL_CONTENT_ROWS, manipulator)
  def forAllContentRows(startIndex:  Int, count:       Int, manipulator: TableRowManipulator): Unit = forAllSectionsRows(startIndex, count, ALL_CONTENT_ROWS, manipulator)
  def forAllSectionRows(manipulator: TableRowManipulator):                                     Unit = forAllSectionsRows(0, Integer.MAX_VALUE, ALL_SECTIONS, manipulator)
  def forAllSectionRows(startIndex:  Int, manipulator: TableRowManipulator):                   Unit = forAllSectionsRows(startIndex, Integer.MAX_VALUE, ALL_SECTIONS, manipulator)
  def forAllSectionRows(startIndex:  Int, count:       Int, manipulator: TableRowManipulator): Unit = forAllSectionsRows(startIndex, count, ALL_SECTIONS, manipulator)
  def forAllHeaderRows(manipulator:  TableRowManipulator):                                     Unit = forAllSectionsRows(0, Integer.MAX_VALUE, ALL_HEADER_ROWS, manipulator)
  def forAllHeaderRows(startIndex:   Int, manipulator: TableRowManipulator):                   Unit = forAllSectionsRows(startIndex, Integer.MAX_VALUE, ALL_HEADER_ROWS, manipulator)
  def forAllHeaderRows(startIndex:   Int, count:       Int, manipulator: TableRowManipulator): Unit = forAllSectionsRows(startIndex, count, ALL_HEADER_ROWS, manipulator)
  def forAllBodyRows(manipulator:    TableRowManipulator):                                     Unit = forAllSectionsRows(0, Integer.MAX_VALUE, ALL_BODY_ROWS, manipulator)
  def forAllBodyRows(startIndex:     Int, manipulator: TableRowManipulator):                   Unit = forAllSectionsRows(startIndex, Integer.MAX_VALUE, ALL_HEADER_ROWS, manipulator)
  def forAllBodyRows(startIndex:     Int, count:       Int, manipulator: TableRowManipulator): Unit = forAllSectionsRows(startIndex, count, ALL_HEADER_ROWS, manipulator)

  def deleteRows(rowIndex: Int, count: Int): Unit =
    if (rowIndex <= header.rows.size()) {
      var i = count
      while (i > 0 && rowIndex < header.rows.size()) {
        header.rows.remove(rowIndex)
        i -= 1
      }
    } else if (rowIndex >= header.rows.size() + separator.rows.size()) {
      val index = rowIndex - header.rows.size() - separator.rows.size()
      var i     = count
      while (i > 0 && index < body.rows.size()) {
        body.rows.remove(index)
        i -= 1
      }
    }

  def insertRows(rowIndex: Int, count: Int): Unit = {
    val maxColumns = getMaxColumns
    if (rowIndex <= header.rows.size()) {
      insertRowsInto(header.rows, rowIndex, count, maxColumns)
    } else {
      insertRowsInto(body.rows, Utils.rangeLimit(rowIndex - header.rows.size() - separator.rows.size(), 0, body.rows.size()), count, maxColumns)
    }
  }

  private def insertRowsInto(rows: ArrayList[TableRow], index: Int, count: Int, maxColumns: Int): Unit = {
    var i = count
    while (i > 0) {
      val emptyRow = new TableRow()
      emptyRow.appendColumns(maxColumns)
      if (index >= rows.size()) rows.add(emptyRow)
      else rows.add(index, emptyRow)
      i -= 1
    }
  }

  def insertColumns(column: Int, count: Int): Unit = {
    forAllContentRows(
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int = {
          sectionRows.get(sectionRowIndex).insertColumns(column, count)
          0
        }
      }
    )
    // insert separator columns separately, since they are not reduced in finalize
    val iter = separator.rows.iterator()
    while (iter.hasNext) iter.next().insertColumns(column, count)
  }

  def deleteColumns(column: Int, count: Int): Unit = {
    forAllContentRows(
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int = {
          sectionRows.get(sectionRowIndex).deleteColumns(column, count)
          0
        }
      }
    )
    val iter = separator.rows.iterator()
    while (iter.hasNext) iter.next().deleteColumns(column, count)
  }

  def moveColumn(fromColumn: Int, toColumn: Int): Unit = {
    forAllContentRows(
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int = {
          sectionRows.get(sectionRowIndex).moveColumn(fromColumn, toColumn)
          0
        }
      }
    )
    val iter = separator.rows.iterator()
    while (iter.hasNext) iter.next().moveColumn(fromColumn, toColumn)
  }

  /** Test all rows for having given column empty. All columns after row's max column are empty
    *
    * @param column
    *   index in allRows list
    * @return
    *   true if column is empty for all rows, separator row excluded
    */
  def isEmptyColumn(column: Int): Boolean = {
    val result = Array(true)
    forAllContentRows(
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int =
          if (!row.isEmptyColumn(column)) { result(0) = false; TableRowManipulator.BREAK }
          else 0
      }
    )
    result(0)
  }

  def isAllRowsEmptyAt(rowIndex:     Int): Boolean = isEmptyRowAt(rowIndex, ALL_TABLE_ROWS)
  def isContentRowsEmptyAt(rowIndex: Int): Boolean = isEmptyRowAt(rowIndex, ALL_CONTENT_ROWS)

  private def isEmptyRowAt(rowIndex: Int, sections: Array[TableSection]): Boolean = {
    val result = Array(false)
    forAllSectionsRows(
      rowIndex,
      1,
      sections,
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int = {
          if (row.isEmpty) result(0) = true
          TableRowManipulator.BREAK
        }
      }
    )
    result(0)
  }

  /*
   * Used during table construction by building the table
   * as the AST visiting process (Flexmark or HTML)
   */
  def getHeader:                        Boolean = isHeading
  def setHeader(header:       Boolean): Unit    = isHeading = header
  def isSeparator:                      Boolean = _isSeparator
  def setSeparator(separator: Boolean): Unit    = _isSeparator = separator

  def setHeader():    Unit = { isHeading = true; _isSeparator = false }
  def setSeparator(): Unit = { _isSeparator = true; isHeading = false }
  def setBody():      Unit = { _isSeparator = false; isHeading = false }

  def nextRow(): Unit = {
    if (_isSeparator) throw new IllegalStateException("Only one separator row allowed")
    if (isHeading) header.nextRow() else body.nextRow()
  }

  /** @param cell
    *   cell to add
    */
  def addCell(cell: TableCell): Unit = {
    val tableSection = if (_isSeparator) separator else if (isHeading) header else body

    if (_isSeparator && (cell.columnSpan != 1 || cell.rowSpan != 1)) {
      throw new IllegalStateException("Separator columns cannot span rows/columns")
    }

    val currentRow = tableSection.get(tableSection.row)

    // skip cells that are already set
    while (tableSection.column < currentRow.cells.size() && currentRow.cells.get(tableSection.column) != null)
      tableSection.column += 1

    var rowSpan = 0
    while (rowSpan < cell.rowSpan) {
      tableSection.get(tableSection.row + rowSpan).set(tableSection.column, cell)

      // set the rest to NULL cells up to null column
      var columnSpan = 1
      while (columnSpan < cell.columnSpan) {
        tableSection.expandTo(tableSection.row + rowSpan, tableSection.column + columnSpan)
        if (tableSection.get(tableSection.row + rowSpan).cells.get(tableSection.column + columnSpan) != null) {
          columnSpan = cell.columnSpan // break
        } else {
          tableSection.rows.get(tableSection.row + rowSpan).set(tableSection.column + columnSpan, TableCell.NULL)
          columnSpan += 1
        }
      }
      rowSpan += 1
    }

    tableSection.column += cell.columnSpan
  }

  def normalize(): Unit = {
    header.normalize()
    separator.normalize()
    body.normalize()
  }

  def finalizeTable(): Unit = {
    // remove null cells
    normalize()

    if (options.fillMissingColumns) {
      fillMissingColumns(Nullable(options.formatTableFillMissingMinColumn.getOrElse(null)))
    }

    val sepColumns = getMaxColumns
    alignments = new Array[CellAlignment](sepColumns)
    columnWidths = new Array[Int](sepColumns)
    val spanAlignment = new BitSet(sepColumns)
    var columnSpans   = new ArrayList[MarkdownTable.ColumnSpan]()
    val delta         = new Ref[Integer](0)

    if (separator.rows.size() > 0) {
      val row      = separator.rows.get(0)
      var jSpan    = 0
      val cellIter = row.cells.iterator()
      while (cellIter.hasNext) {
        val cell = cellIter.next()
        // set alignment if not already set or was set by a span and this column is not a span
        if ((alignments(jSpan) == null || cell.columnSpan == 1 && spanAlignment.get(jSpan)) && cell.alignment != CellAlignment.NONE) {
          alignments(jSpan) = cell.alignment
          if (cell.columnSpan > 1) spanAlignment.set(jSpan)
        }
        jSpan += cell.columnSpan
      }
    }

    if (header.rows.size() > 0) {
      val rowIter = header.rows.iterator()
      while (rowIter.hasNext) {
        val row   = rowIter.next()
        var j     = 0
        var jSpan = 0
        val kMax  = row.cells.size()
        var k     = 0
        while (k < kMax) {
          val cell = row.cells.get(k)
          if ((alignments(jSpan) == null || cell.columnSpan == 1 && spanAlignment.get(jSpan)) && cell.alignment != CellAlignment.NONE) {
            alignments(jSpan) = cell.alignment
            if (cell.columnSpan > 1) spanAlignment.set(jSpan)
          }

          delta.value = 0
          val cellText = this.cellText(row.cells, k, false, true, 0, null, delta)
          val width    = options.charWidthProvider.getStringWidth(cellText) + options.spacePad + options.pipeWidth * cell.columnSpan
          if (cell.columnSpan > 1) {
            columnSpans.add(new MarkdownTable.ColumnSpan(j, cell.columnSpan, width))
          } else {
            if (columnWidths(jSpan) < width) columnWidths(jSpan) = width
          }

          j += 1
          jSpan += cell.columnSpan
          k += 1
        }
      }
    }

    if (body.rows.size() > 0) {
      val rowIter = body.rows.iterator()
      while (rowIter.hasNext) {
        val row   = rowIter.next()
        var jSpan = 0
        val kMax  = row.cells.size()
        var k     = 0
        while (k < kMax) {
          val cell = row.cells.get(k)
          delta.value = 0
          val cellText = this.cellText(row.cells, k, false, false, 0, null, delta)
          val width    = options.charWidthProvider.getStringWidth(cellText) + options.spacePad + options.pipeWidth * cell.columnSpan
          if (cell.columnSpan > 1) {
            columnSpans.add(new MarkdownTable.ColumnSpan(jSpan, cell.columnSpan, width))
          } else {
            if (columnWidths(jSpan) < width) columnWidths(jSpan) = width
          }

          jSpan += cell.columnSpan
          k += 1
        }
      }
    }

    // add separator column widths to the calculation
    if (separator.rows.size() == 0 || body.rows.size() > 0 || header.rows.size() > 0) {
      var j = 0
      delta.value = 0
      for (alignment <- alignments) {
        val alignment1 = adjustCellAlignment(alignment)
        val colonCount = if (alignment1 == CellAlignment.LEFT || alignment1 == CellAlignment.RIGHT) 1 else if (alignment1 == CellAlignment.CENTER) 2 else 0
        val dashCount0 = 0
        val dashesOnly = Utils.minLimit(dashCount0, options.minSeparatorColumnWidth - colonCount, options.minSeparatorDashes)
        val dashCount  = if (dashCount0 < dashesOnly) dashesOnly else dashCount0
        val width      = dashCount * options.dashWidth + colonCount * options.colonWidth + options.pipeWidth
        if (columnWidths(j) < width) columnWidths(j) = width
        j += 1
      }
    } else {
      // keep as is
      var j = 0
      delta.value = 0
      val cellIter = separator.rows.get(0).cells.iterator()
      while (cellIter.hasNext) {
        val cell       = cellIter.next()
        val alignment  = adjustCellAlignment(cell.alignment)
        val colonCount = if (alignment == CellAlignment.LEFT || alignment == CellAlignment.RIGHT) 1 else if (alignment == CellAlignment.CENTER) 2 else 0
        val trim       = cell.text.trim(MarkdownTable.COLON_TRIM_CHARS)
        val dashCount0 = trim.length()
        val dashesOnly = Utils.minLimit(dashCount0, options.minSeparatorColumnWidth - colonCount, options.minSeparatorDashes)
        val dashCount  = if (dashCount0 < dashesOnly) dashesOnly else dashCount0
        val width      = dashCount * options.dashWidth + colonCount * options.colonWidth + options.pipeWidth
        if (columnWidths(j) < width) columnWidths(j) = width
        j += 1
      }
    }

    if (!columnSpans.isEmpty) {
      // now need to distribute extra width from spans to contained columns
      val unfixedColumns = new BitSet(sepColumns)
      var newColumnSpans = new ArrayList[MarkdownTable.ColumnSpan](columnSpans.size())

      val spanIter1 = columnSpans.iterator()
      while (spanIter1.hasNext) {
        val columnSpan = spanIter1.next()
        val sw         = spanWidth(columnSpan.startColumn, columnSpan.columnSpan)
        if (sw < columnSpan.width) {
          unfixedColumns.set(columnSpan.startColumn, columnSpan.startColumn + columnSpan.columnSpan)
          newColumnSpans.add(columnSpan)
        }
      }

      // we now distribute additional width equally between columns that are spanned to unfixed columns
      while (!newColumnSpans.isEmpty) {
        columnSpans = newColumnSpans
        val fixedColumns = new BitSet(sepColumns)
        newColumnSpans = new ArrayList[MarkdownTable.ColumnSpan]()

        // remove spans that already fit into fixed columns
        val spanIter2 = columnSpans.iterator()
        while (spanIter2.hasNext) {
          val columnSpan = spanIter2.next()
          val sw         = spanWidth(columnSpan.startColumn, columnSpan.columnSpan)
          val fw         = spanFixedWidth(unfixedColumns, columnSpan.startColumn, columnSpan.columnSpan)
          if (sw <= fw) {
            fixedColumns.set(columnSpan.startColumn, columnSpan.startColumn + columnSpan.columnSpan)
          } else {
            newColumnSpans.add(columnSpan)
          }
        }

        // reset fixed columns
        unfixedColumns.andNot(fixedColumns)
        columnSpans = newColumnSpans
        newColumnSpans = new ArrayList[MarkdownTable.ColumnSpan]()

        val spanIter3 = columnSpans.iterator()
        while (spanIter3.hasNext) {
          val columnSpan = spanIter3.next()
          val sw         = spanWidth(columnSpan.startColumn, columnSpan.columnSpan)
          val fw         = spanFixedWidth(unfixedColumns, columnSpan.startColumn, columnSpan.columnSpan)
          if (sw > fw) {
            val distributeWidth    = sw - fw
            val unfixedColumnCount = unfixedColumns.get(columnSpan.startColumn, columnSpan.startColumn + columnSpan.columnSpan).cardinality()
            val perSpanWidth       = distributeWidth / unfixedColumnCount
            var extraWidth         = distributeWidth - perSpanWidth * unfixedColumnCount

            var i = 0
            while (i < columnSpan.columnSpan) {
              if (unfixedColumns.get(columnSpan.startColumn + i)) {
                columnWidths(columnSpan.startColumn + i) += perSpanWidth
                if (extraWidth > 0) {
                  columnWidths(columnSpan.startColumn + i) += 1
                  extraWidth -= 1
                }
              }
              i += 1
            }
            newColumnSpans.add(columnSpan)
          }
        }
      }
    }
  }

