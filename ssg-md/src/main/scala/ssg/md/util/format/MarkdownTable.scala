  def formattedCaption(
    caption: BasedSequence,
    options: TableFormatOptions
  ): Nullable[String] = {
    var append = caption.isNotNull

    options.formatTableCaption match {
      case options.formatTableCaption if options.formatTableCaption == ssg.md.util.format.options.TableCaptionHandling.ADD =>
        append = true
      case options.formatTableCaption if options.formatTableCaption == ssg.md.util.format.options.TableCaptionHandling.REMOVE_EMPTY =>
        append = !caption.isBlank()
      case options.formatTableCaption if options.formatTableCaption == ssg.md.util.format.options.TableCaptionHandling.REMOVE =>
        append = false
      case _ => // AS_IS
    }

    if (append) {
      val captionSpaces = options.formatTableCaptionSpaces match {
        case DiscretionaryText.ADD    => " "
        case DiscretionaryText.REMOVE => ""
        case _                        => ""
      }
      Nullable(captionSpaces + caption.toString + captionSpaces)
    } else {
      Nullable(null)
    }
  }

