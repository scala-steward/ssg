  /** Transpose table
    */
  def transposed(columnHeaders: Int): MarkdownTable = {
    val tr = new MarkdownTable(tableChars, options)
    tr.trackedOffsets.addAll(trackedOffsets)

    val maxRows    = getAllRowsCount - 1 // don't count separator rows
    val maxCols    = getMaxColumns
    val tableCells = Array.ofDim[TableCell](maxRows, maxCols)

    // get a matrix of all cells
    forAllSectionsRows(
      0,
      Integer.MAX_VALUE,
      ALL_CONTENT_ROWS,
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int = {
          val tableCellRow = tableCells(allRowsIndex)
          val iMax         = row.cells.size()
          var col          = 0
          var i            = 0
          while (i < iMax) {
            val cell = row.cells.get(i)
            var span = 0
            while (span < cell.columnSpan) {
              tableCellRow(col) = new TableCell(cell, span == 0, 1, 1, null)
              col += 1
              span += 1
            }
            i += 1
          }
          0
        }
      }
    )

    tr.setHeader()
    val colHdrs = Math.min(Math.max(0, columnHeaders), maxCols)
    var c       = 0
    while (c < colHdrs) {
      var r = 0
      while (r < maxRows) {
        val cell = tableCells(r)(c)
        tr.addCell(if (cell == null) TableCell.DEFAULT_CELL else cell)
        r += 1
      }
      tr.nextRow()
      c += 1
    }

    val sepRow = separator.rows.get(0)
    tr.setSeparator()
    val iMax = sepRow.cells.size()
    var i    = 0
    while (i < maxRows) {
      if (i < iMax) {
        tr.addCell(new TableCell(sepRow.cells.get(i), true, 1, 1, null))
      } else {
        tr.addCell(new TableCell("---", 1, 1))
      }
      i += 1
    }

    tr.setBody()
    c = colHdrs
    while (c < maxCols) {
      var r = 0
      while (r < maxRows) {
        val cell = tableCells(r)(c)
        tr.addCell(if (cell == null) TableCell.DEFAULT_CELL else cell)
        r += 1
      }
      tr.nextRow()
      c += 1
    }

    tr.setCaptionCell(getCaptionCell)
    tr
  }

  /** Sort table
    */
  def sorted(columnSorts: Array[ColumnSort], textCollectionFlags: Int, numericSuffixTester: Nullable[NumericSuffixPredicate]): MarkdownTable = {
    val sortedTable = new MarkdownTable(tableChars, options)
    sortedTable.trackedOffsets.addAll(trackedOffsets)

    sortedTable.setHeader()
    forAllSectionsRows(
      0,
      Integer.MAX_VALUE,
      ALL_HEADER_ROWS,
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int = {
          val iMax = row.cells.size()
          var i    = 0
          while (i < iMax) {
            val cell = row.cells.get(i)
            sortedTable.addCell(if (cell eq TableCell.DEFAULT_CELL) cell else new TableCell(cell, true, cell.rowSpan, cell.columnSpan, cell.alignment))
            i += 1
          }
          sortedTable.nextRow()
          0
        }
      }
    )

    sortedTable.setSeparator()
    val sepRow         = separator.rows.get(0)
    val sepIMax        = sepRow.cells.size()
    val sortAlignments = new Array[CellAlignment](sepIMax)

    var si = 0
    while (si < sepIMax) {
      val cell = sepRow.cells.get(si)
      sortedTable.addCell(if (cell eq TableCell.DEFAULT_CELL) cell else new TableCell(cell, true, cell.rowSpan, cell.columnSpan, cell.alignment))
      sortAlignments(si) = cell.alignment
      si += 1
    }

    val rows      = getAllSectionsRows(body)
    val cellSizes = new Array[Int](sepIMax)

    val rMax = rows.size()
    val cMax = getMaxBodyColumns

    var r = 0
    while (r < rMax) {
      for (columnSort <- columnSorts) {
        val c = columnSort.column
        if (c >= 0 && c < cMax) {
          val spanIndex = rows.get(r).indexOf(c)
          val cell      = rows.get(r).cells.get(spanIndex.index)
          if (spanIndex.index == c && cell != null) {
            // not in span
            cellSizes(c) = Math.max(cellSizes(c), cell.text.length())
          }
        }
      }
      r += 1
    }

    val visitor = new TextCollectingVisitor()
    val numericSuffixPredicate: NumericSuffixPredicate = if (numericSuffixTester.isEmpty) MarkdownTable.ALL_SUFFIXES_SORT else numericSuffixTester.get

    val rowComparator: Comparator[TableRow] = (o1: TableRow, o2: TableRow) => {
      var result = 0
      boundary {
        for (columnSort <- columnSorts) {
          val c = columnSort.column
          if (c >= 0 && c < cMax) {
            val cellSize = cellSizes(c)
            if (cellSize > 0) {
              val sort        = columnSort.sort
              var descending  = sort.isDescending
              val numeric     = sort.isNumeric
              val numericLast = sort.isNumericLast

              val spanIndex1 = o1.indexOf(c)
              val cell1      = o1.cells.get(spanIndex1.index)
              val spanIndex2 = o2.indexOf(c)
              val cell2      = o2.cells.get(spanIndex2.index)

              if (spanIndex1.index == c && cell1 != null && spanIndex2.index == c && cell2 != null) {
                var padLeft1  = 0; var padLeft2 = 0; var padRight1 = 0; var padRight2 = 0
                val cellText1 = if (cell1.tableCellNode.isEmpty) cell1.text.toString else visitor.collectAndGetText(cell1.tableCellNode.get, textCollectionFlags).trim
                val cellText2 = if (cell2.tableCellNode.isEmpty) cell2.text.toString else visitor.collectAndGetText(cell2.tableCellNode.get, textCollectionFlags).trim
                val diff1     = cellSize - cellText1.length
                val diff2     = cellSize - cellText2.length

                sortAlignments(c) match {
                  case CellAlignment.CENTER =>
                    padLeft1 = diff1 >> 1; padRight1 = cellSize - padLeft1
                    padLeft2 = diff2 >> 1; padRight2 = cellSize - padLeft2
                  case CellAlignment.RIGHT =>
                    padLeft1 = diff1; padLeft2 = diff2
                  case _ =>
                }

                if (numeric) {
                  val cellNumeric1 = SequenceUtils.parseNumberPrefixOrNull(Nullable(cellText1), Nullable(numericSuffixPredicate))
                  val cellNumeric2 = SequenceUtils.parseNumberPrefixOrNull(Nullable(cellText2), Nullable(numericSuffixPredicate))

                  if (cellNumeric1.isDefined && cellNumeric2.isDefined) {
                    result = Utils.compare(cellNumeric1.get.first, cellNumeric2.get.first)
                    val numericSuffix1 = cellNumeric1.get.second.get
                    val numericSuffix2 = cellNumeric2.get.second.get

                    if (result == 0 && (numericSuffixPredicate.sortSuffix(numericSuffix1) || numericSuffixPredicate.sortSuffix(numericSuffix2))) {
                      if (numericSuffix1.nonEmpty && numericSuffixPredicate.sortSuffix(numericSuffix1) && numericSuffix2.nonEmpty && numericSuffixPredicate.sortSuffix(numericSuffix2)) {
                        result = numericSuffix1.compareTo(numericSuffix2)
                      } else if (numericSuffix1.nonEmpty && numericSuffixPredicate.sortSuffix(numericSuffix1)) {
                        result = if (numericLast) -1 else 1
                        descending = false
                      } else if (numericSuffix2.nonEmpty && numericSuffixPredicate.sortSuffix(numericSuffix2)) {
                        result = if (numericLast) 1 else -1
                        descending = false
                      }
                    }
                  } else if (cellNumeric1.isDefined) {
                    result = if (numericLast) 1 else -1; descending = false
                  } else if (cellNumeric2.isDefined) {
                    result = if (numericLast) -1 else 1; descending = false
                  } else {
                    val cell1Text = RepeatedSequence.ofSpaces(padLeft1).toString + cellText1 + RepeatedSequence.ofSpaces(padRight1)
                    val cell2Text = RepeatedSequence.ofSpaces(padLeft2).toString + cellText2 + RepeatedSequence.ofSpaces(padRight2)
                    result = cell1Text.compareTo(cell2Text)
                  }
                } else {
                  val cell1Text = RepeatedSequence.ofSpaces(padLeft1).toString + cellText1 + RepeatedSequence.ofSpaces(padRight1)
                  val cell2Text = RepeatedSequence.ofSpaces(padLeft2).toString + cellText2 + RepeatedSequence.ofSpaces(padRight2)
                  result = cell1Text.compareTo(cell2Text)
                }
              } else if (spanIndex1.index == c && cell1 != null) {
                result = 1
              } else if (spanIndex2.index == c && cell2 != null) {
                result = -1
              } else {
                result = 0
              }

              if (result != 0) break(if (descending) -result else result)
            }
          }
        }
        0
      }
    }

    rows.sort(rowComparator)

    sortedTable.setBody()
    var ri = 0
    while (ri < rows.size()) {
      val row   = rows.get(ri)
      val riMax = row.cells.size()
      var ii    = 0
      while (ii < riMax) {
        val cell = row.cells.get(ii)
        sortedTable.addCell(if (cell eq TableCell.DEFAULT_CELL) cell else new TableCell(cell, true, cell.rowSpan, cell.columnSpan, cell.alignment))
        ii += 1
      }
      sortedTable.nextRow()
      ri += 1
    }

    sortedTable.setCaptionCell(getCaptionCell)
    sortedTable
  }

  private[format] def appendDashes(out: LineAppendable, dashCount: Int, sepDashes: BasedSequence, dashOffset: Int): Int = {
    val sepDashesLength = sepDashes.length()
    var remainingDashes = Math.max(0, sepDashesLength - dashOffset)

    if (remainingDashes >= dashCount) {
      out.append(sepDashes.subSequence(dashOffset, dashOffset + dashCount))
      remainingDashes -= dashCount
    } else {
      var usedUpDashes = 0
      if (remainingDashes > 1) {
        out.append(sepDashes.subSequence(dashOffset, dashOffset + 1))
        remainingDashes -= 1
        usedUpDashes += 1
      }
      out.append('-', dashCount - Math.max(0, remainingDashes + usedUpDashes))
      if (remainingDashes > 0) {
        out.append(sepDashes.subSequence(dashOffset, dashOffset + remainingDashes))
        remainingDashes = 0
      }
    }
    sepDashesLength - remainingDashes
  }

  def appendTable(out: LineAppendable): Unit = {
    // we will prepare the separator based on max columns
    val linePrefix = formatTableIndentPrefix

    trackedOffsets.sort(Comparator.comparing[TrackedOffset, Integer]((t: TrackedOffset) => t.offset))

    out.pushOptions()
    out.removeOptions(LineAppendable.F_WHITESPACE_REMOVAL)

    finalizeTable()

    appendRows(out, header.rows, true, linePrefix)

    {
      out.append(linePrefix)
      val row = if (separator.rows.size() > 0) separator.rows.get(0) else null

      if (row != null && row.beforeOffset != TableCell.NOT_TRACKED) {
        setTrackedOffsetIndex(row.beforeOffset, out.offsetWithPending())
      }

      var j     = 0
      val delta = new Ref[Integer](0)
      for (alignment <- alignments) {
        val alignment1 = adjustCellAlignment(alignment)

        val colonCount = if (alignment1 == CellAlignment.LEFT || alignment1 == CellAlignment.RIGHT) 1 else if (alignment1 == CellAlignment.CENTER) 2 else 0
        val diff       = columnWidths(j) - colonCount * options.colonWidth - options.pipeWidth
        var dashCount  = (delta.value + diff) / options.dashWidth
        val dashesOnly = Utils.minLimit(dashCount, options.minSeparatorColumnWidth - colonCount, options.minSeparatorDashes)
        if (dashCount < dashesOnly) dashCount = dashesOnly

        if (Math.abs(delta.value + diff - (dashCount + 1) * options.dashWidth) < Math.abs(delta.value + diff - dashCount * options.dashWidth)) {
          dashCount += 1
        }

        delta.value = delta.value + diff - dashCount * options.dashWidth

        var trackedPos = 0
        var cell:         TableCell = null
        var previousCell: TableCell = null

        if (row != null) {
          val cells = row.cells
          if (j < cells.size()) {
            cell = cells.get(j)
            if (j > 0) previousCell = cells.get(j - 1)
          }
        }

        trackedPos = if (cell == null) TableCell.NOT_TRACKED else minLimit(cell.trackedTextOffset, 0)
        val sepText       = if (cell == null) BasedSequence.NULL else cell.text.trim(MarkdownTable.COLON_TRIM_CHARS)
        var sepDashOffset = 0

        if (trackedPos != TableCell.NOT_TRACKED) {
          if (options.leadTrailPipes && j == 0) out.append('|')
          val beforeFirstColon = trackedPos == 0 && cell.text.charAt(trackedPos) == ':'
          val afterFirstColon  = trackedPos == 1 && cell.text.charAt(trackedPos - 1) == ':'
          val beforeLastColon  = trackedPos == cell.text.length() - 1 && cell.text.charAt(trackedPos) == ':'
          val afterLastColon   = trackedPos == cell.text.length() && cell.text.charAt(trackedPos - 1) == ':'
          val afterLastDash    = trackedPos == cell.text.length() && cell.text.charAt(trackedPos - 1) == '-'

          if (alignment1 == CellAlignment.LEFT || alignment1 == CellAlignment.CENTER) {
            if (beforeFirstColon) {
              setTrackedOffsetIndex(cell.trackedTextOffset + cell.getInsideStartOffset(Nullable(previousCell)), out.offsetWithPending())
              trackedPos = TableCell.NOT_TRACKED
              out.append(':')
            } else if (afterFirstColon) {
              out.append(':')
              setTrackedOffsetIndex(cell.trackedTextOffset + cell.getInsideStartOffset(Nullable(previousCell)), out.offsetWithPending())
              trackedPos = TableCell.NOT_TRACKED
            } else {
              out.append(':')
            }
          } else {
            // no left colon
          }

          if (!afterFirstColon && !beforeFirstColon && !afterLastColon && !beforeLastColon) {
            if (trackedPos == 0) {
              setTrackedOffsetIndex(cell.trackedTextOffset + cell.getInsideStartOffset(Nullable(previousCell)), out.offsetWithPending())
              trackedPos = TableCell.NOT_TRACKED
              sepDashOffset = appendDashes(out, dashCount, sepText, sepDashOffset)
            } else if (!afterLastDash && trackedPos < dashCount) {
              sepDashOffset = appendDashes(out, trackedPos, sepText, sepDashOffset)
              setTrackedOffsetIndex(cell.trackedTextOffset + cell.getInsideStartOffset(Nullable(previousCell)), out.offsetWithPending())
              sepDashOffset = appendDashes(out, dashCount - trackedPos, sepText, sepDashOffset)
              trackedPos = TableCell.NOT_TRACKED
            } else {
              sepDashOffset = appendDashes(out, dashCount, sepText, sepDashOffset)
              setTrackedOffsetIndex(cell.trackedTextOffset + cell.getInsideStartOffset(Nullable(previousCell)), out.offsetWithPending())
              trackedPos = TableCell.NOT_TRACKED
            }
          } else {
            sepDashOffset = appendDashes(out, dashCount, sepText, sepDashOffset)
          }

          if (alignment1 == CellAlignment.RIGHT || alignment1 == CellAlignment.CENTER) {
            if (afterLastColon) {
              out.append(':')
              setTrackedOffsetIndex(cell.trackedTextOffset + cell.getInsideStartOffset(Nullable(previousCell)), out.offsetWithPending())
              trackedPos = TableCell.NOT_TRACKED
            } else if (beforeLastColon) {
              setTrackedOffsetIndex(cell.trackedTextOffset + cell.getInsideStartOffset(Nullable(previousCell)), out.offsetWithPending())
              trackedPos = TableCell.NOT_TRACKED
              out.append(':')
            } else {
              out.append(':')
            }
          } else if (afterLastColon || beforeLastColon) {
            setTrackedOffsetIndex(cell.trackedTextOffset + cell.getInsideStartOffset(Nullable(previousCell)), out.offsetWithPending())
            trackedPos = TableCell.NOT_TRACKED
          }

          assert(trackedPos == TableCell.NOT_TRACKED)
        } else {
          if (options.leadTrailPipes && j == 0) out.append('|')
          if (alignment1 == CellAlignment.LEFT || alignment1 == CellAlignment.CENTER) { out.append(':') }
          sepDashOffset = appendDashes(out, dashCount, sepText, sepDashOffset)
          if (alignment1 == CellAlignment.RIGHT || alignment1 == CellAlignment.CENTER) { out.append(':') }
        }

        j += 1
        if (options.leadTrailPipes || j < alignments.length) out.append('|')
      }

      if (row != null && row.afterOffset != TableCell.NOT_TRACKED) {
        setTrackedOffsetIndex(row.afterOffset, out.offsetWithPending())
      }

      out.line()
    }

    appendRows(out, body.rows, false, linePrefix)

    val captionCell = getCaptionCell
    val captionText = MarkdownTable.formattedCaption(captionCell.text, options)
    if (captionText.isDefined) {
      val formattedCaption = BasedSequence.of(captionText.get).subSequence(0, captionText.get.length)
      var handled          = false

      if (this.caption.rows.size() > 0) {
        val row = this.caption.rows.get(0)
        if (captionCell.trackedTextOffset != TableCell.NOT_TRACKED || row.beforeOffset != TableCell.NOT_TRACKED || row.afterOffset != TableCell.NOT_TRACKED) {
          val cell = captionCell

          out.line()

          if (row.beforeOffset != TableCell.NOT_TRACKED) {
            setTrackedOffsetIndex(row.beforeOffset, out.offsetWithPending())
          }

          var adjCaptionCell = captionCell.withText(captionCell.text.trim())
          if (cell.trackedTextOffset != TableCell.NOT_TRACKED) {
            adjCaptionCell = adjCaptionCell.withTrackedOffset(minLimit(cell.trackedTextOffset - cell.text.trimmedStart().length(), 0))
          }

          var addOpenCaptionSpace  = false
          var addCloseCaptionSpace = false

          if (!adjCaptionCell.text.isBlank()) {
            options.formatTableCaptionSpaces match {
              case DiscretionaryText.ADD =>
                addOpenCaptionSpace = true
                addCloseCaptionSpace = true
              case DiscretionaryText.REMOVE =>
              // nothing
              case _ => // AS_IS
                addOpenCaptionSpace = cell.text.startsWith(" ")
                addCloseCaptionSpace = cell.text.endsWith(" ")
            }
          }

          out.append(linePrefix)
          out.append('[')
          if (addOpenCaptionSpace) out.append(' ')

          val cellOffset = out.offsetWithPending()

          row.cells.set(0, adjCaptionCell)
          val delta    = new Ref[Integer](0)
          val cellText = this.cellText(row.cells, 0, true, false, 0, CellAlignment.LEFT, delta)

          if (row.cells.size() > 0) {
            if (cell.trackedTextOffset != TableCell.NOT_TRACKED) {
              val adjustedCell = row.cells.get(0)
              if (adjustedCell.trackedTextOffset != TableCell.NOT_TRACKED) {
                setTrackedOffsetIndex(
                  cell.trackedTextOffset + cell.text.startOffset,
                  cellOffset + (if (cellText.isBlank()) 1 else minLimit(adjustedCell.trackedTextOffset, 0) + adjustedCell.trackedTextAdjust)
                )
              }
            }
            row.cells.set(0, cell)
          } else {
            row.cells.add(cell)
          }

          out.append(cellText)
          if (addCloseCaptionSpace) out.append(' ')
          out.append(']')

          if (row.afterOffset != TableCell.NOT_TRACKED) {
            setTrackedOffsetIndex(row.afterOffset, out.offsetWithPending())
          }
          out.line()
          handled = true
        }
      }

      if (!handled) {
        out.popOptions().pushOptions()
        out.line().append(linePrefix).append('[').append(formattedCaption).append(']').line()
      }
    }
    out.popOptions()
  }

  private def pipeNeedsSpaceBefore(cell: TableCell): Boolean =
    cell.text.equals(" ") || !cell.text.endsWith(" ")

  private def pipeNeedsSpaceAfter(cell: TableCell): Boolean =
    cell.text.equals(" ") || !cell.text.startsWith(" ")

  private def appendRows(
    out:        LineAppendable,
    rows:       JList[TableRow],
    isHeader:   Boolean,
    linePrefix: CharSequence
  ): Unit = {
    val rowIter = rows.iterator()
    while (rowIter.hasNext) {
      val row   = rowIter.next()
      var j     = 0
      var jSpan = 0
      val delta = new Ref[Integer](0)

      out.append(linePrefix)

      if (row.beforeOffset != TableCell.NOT_TRACKED) {
        setTrackedOffsetIndex(row.beforeOffset, out.offsetWithPending())
      }

      val iMax = row.cells.size()
      var i    = 0
      while (i < iMax) {
        val cell = row.cells.get(i)

        if (j == 0) {
          if (options.leadTrailPipes) {
            out.append('|')
            if (options.spaceAroundPipes && pipeNeedsSpaceAfter(cell)) { out.append(' ') }
          }
        } else {
          if (options.spaceAroundPipes && pipeNeedsSpaceAfter(cell)) out.append(' ')
        }

        val cellAlignment = if (isHeader && cell.alignment != CellAlignment.NONE) cell.alignment else alignments(jSpan)

        val cellText = this.cellText(
          row.cells,
          i,
          true,
          isHeader,
          spanWidth(jSpan, cell.columnSpan) - options.spacePad - options.pipeWidth * cell.columnSpan,
          cellAlignment,
          delta
        )

        if (cell.trackedTextOffset != TableCell.NOT_TRACKED) {
          val adjustedCell = row.cells.get(i)
          if (adjustedCell.trackedTextOffset != TableCell.NOT_TRACKED) {
            val cellOffset     = out.offsetWithPending()
            val adjustForBlank = if (cell.text.isBlank()) -1 else 0
            if (
              !setTrackedOffsetIndex(
                cell.trackedTextOffset + cell.getTextStartOffset(if (i == 0) Nullable(null) else Nullable(row.cells.get(i - 1))),
                cellOffset + minLimit(adjustedCell.trackedTextOffset + adjustForBlank, 0) + adjustedCell.trackedTextAdjust
              )
            ) {
              // QUERY: Triggered after sort table in MdNav for header row
              System.out.println(
                String.format(
                  "Offset not found: cell.trackedTextOffset: %d, adjusted trackedOffset: %d in offsets: %s",
                  cell.trackedTextOffset.asInstanceOf[AnyRef],
                  (cell.trackedTextOffset + cell.getTextStartOffset(if (i == 0) Nullable(null) else Nullable(row.cells.get(i - 1)))).asInstanceOf[AnyRef],
                  trackedOffsets
                )
              )
            }
          }
        }

        out.append(cellText)

        j += 1
        jSpan += cell.columnSpan

        if (j < alignments.length) {
          if (options.spaceAroundPipes && pipeNeedsSpaceBefore(cell)) out.append(' ')
          appendColumnSpan(out, cell.columnSpan, cell.getInsideEndOffset, cell.spanTrackedOffset)
        } else if (options.leadTrailPipes) {
          if (options.spaceAroundPipes && pipeNeedsSpaceBefore(cell)) out.append(' ')
          appendColumnSpan(out, cell.columnSpan, cell.getInsideEndOffset, cell.spanTrackedOffset)
        } else {
          if (options.spaceAroundPipes && pipeNeedsSpaceBefore(cell)) out.append(' ')
          appendColumnSpan(out, cell.columnSpan - 1, cell.getInsideEndOffset, cell.spanTrackedOffset)
        }

        i += 1
      }

      if (row.afterOffset != TableCell.NOT_TRACKED) {
        setTrackedOffsetIndex(row.afterOffset, out.offsetWithPending())
      }

      if (j > 0) out.line()
    }
  }

  private def appendColumnSpan(out: LineAppendable, span: Int, cellInsideEndOffset: Int, trackedSpanOffset: Int): Unit =
    if (trackedSpanOffset == TableCell.NOT_TRACKED) {
      out.append('|', span)
    } else {
      if (trackedSpanOffset == 0) {
        setTrackedOffsetIndex(cellInsideEndOffset + trackedSpanOffset, out.offsetWithPending())
        out.append('|', span)
      } else if (trackedSpanOffset < span) {
        out.append('|', trackedSpanOffset)
        setTrackedOffsetIndex(cellInsideEndOffset + trackedSpanOffset, out.offsetWithPending())
        out.append('|', span - trackedSpanOffset)
      } else {
        out.append('|', span)
        setTrackedOffsetIndex(cellInsideEndOffset + trackedSpanOffset, out.offsetWithPending())
      }
    }

  private def cellText(
    cells:             JList[TableCell],
    index:             Int,
    withTrackedOffset: Boolean,
    isHeader:          Boolean,
    width:             Int,
    alignment:         CellAlignment,
    delta:             Ref[Integer]
  ): BasedSequence = {
    val cell         = cells.get(index)
    var adjustedCell = cell
    var text         = cell.text
    val needsPadding = cell.trackedTextOffset != TableCell.NOT_TRACKED && cell.trackedTextOffset >= cell.text.length()
    var neededPrefix = false

    if (cell.trackedTextOffset != TableCell.NOT_TRACKED) {
      if (cell.trackedTextOffset > cell.text.length()) {
        val suffixed = cell.trackedTextOffset - cell.text.length() - 1
        text = text.append(RepeatedSequence.repeatOf(' ', suffixed))
      } else if (cell.trackedTextOffset < 0) {
        neededPrefix = true
      }
    }

    val length       = options.charWidthProvider.getStringWidth(text)
    var useAlignment = alignment
    if (options.adjustColumnWidth && (length < width || cell.trackedTextOffset > cell.text.length())) {
      if (!options.applyColumnAlignment || useAlignment == null || useAlignment == CellAlignment.NONE) {
        useAlignment = if (isHeader && options.leftAlignMarker != DiscretionaryText.ADD) CellAlignment.CENTER else CellAlignment.LEFT
      } else if (isHeader && useAlignment == CellAlignment.LEFT && options.leftAlignMarker == DiscretionaryText.REMOVE) {
        useAlignment = CellAlignment.CENTER
      }

      val diff       = width - length
      var spaceCount = (delta.value + diff) / options.spaceWidth
      // NOTE: add extra space if accumulated adding extra space gives smaller abs error
      if (width > 0 && Math.abs(delta.value + diff - (spaceCount + 1) * options.spaceWidth) < Math.abs(delta.value + diff - spaceCount * options.spaceWidth)) {
        spaceCount += 1
      }

      delta.value = delta.value + diff - spaceCount * options.spaceWidth

      useAlignment match {
        case CellAlignment.LEFT =>
          if (spaceCount > 0) {
            text = text.append(PrefixedSubSequence.repeatOf(" ", spaceCount, text.getEmptySuffix))
          }
          if (withTrackedOffset && needsPadding && cell.afterSpace) {
            if (spaceCount <= 0) { adjustedCell = adjustedCell.withTrackedTextAdjust(1) }
          }

        case CellAlignment.RIGHT =>
          if (spaceCount > 0) {
            text = PrefixedSubSequence.repeatOf(" ", spaceCount, text)
            if (withTrackedOffset && cell.trackedTextOffset != TableCell.NOT_TRACKED) {
              adjustedCell = cell.withTrackedOffset(maxLimit(text.length(), cell.trackedTextOffset + spaceCount))
            }
            if (withTrackedOffset && neededPrefix && cell.afterSpace) {
              adjustedCell = adjustedCell.withTrackedTextAdjust(1)
            }
          }
          if (withTrackedOffset && needsPadding && cell.afterSpace) {
            if (spaceCount <= 0 || !cell.afterDelete) { adjustedCell = adjustedCell.withTrackedTextAdjust(1) }
          }

        case CellAlignment.CENTER =>
          val count = spaceCount / 2
          if (spaceCount > 0) {
            text = PrefixedSubSequence.repeatOf(" ", count, text).append(PrefixedSubSequence.repeatOf(" ", spaceCount - count, text.getEmptySuffix))
            if (withTrackedOffset && cell.trackedTextOffset != TableCell.NOT_TRACKED) {
              adjustedCell = cell.withTrackedOffset(maxLimit(text.length(), cell.trackedTextOffset + count))
            }
            if (withTrackedOffset && neededPrefix && cell.afterSpace) {
              adjustedCell = adjustedCell.withTrackedTextAdjust(1)
            }
          } else {
            if (withTrackedOffset && needsPadding && cell.afterSpace) {
              adjustedCell = adjustedCell.withTrackedTextAdjust(1)
            }
          }

        case _ =>
      }
    }

    if (withTrackedOffset && adjustedCell.trackedTextOffset != TableCell.NOT_TRACKED) {
      // replace with adjusted offset
      if (adjustedCell.trackedTextOffset > text.length()) {
        adjustedCell = adjustedCell.withTrackedOffset(text.length())
      }
      if (adjustedCell ne cell) cells.set(index, adjustedCell)
    }

    text
  }

  private def spanWidth(col: Int, columnSpan: Int): Int =
    if (columnSpan > 1) {
      var width = 0
      var i     = 0
      while (i < columnSpan) { width += columnWidths(i + col); i += 1 }
      width
    } else {
      columnWidths(col)
    }

  private def spanFixedWidth(unfixedColumns: BitSet, col: Int, columnSpan: Int): Int =
    if (columnSpan > 1) {
      var width = 0
      var i     = 0
      while (i < columnSpan) {
        if (!unfixedColumns.get(i)) { width += columnWidths(i + col) }
        i += 1
      }
      width
    } else {
      if (unfixedColumns.get(col)) 0 else columnWidths(col)
    }

  private def adjustCellAlignment(alignment: CellAlignment): CellAlignment = {
    var result = alignment
    options.leftAlignMarker match {
      case DiscretionaryText.ADD =>
        if (result == null || result == CellAlignment.NONE) { result = CellAlignment.LEFT }
      case DiscretionaryText.REMOVE =>
        if (result == CellAlignment.LEFT) result = CellAlignment.NONE
      case _ =>
    }
    result
  }

  private def aggregateTotalColumnsWithoutColumns(
    sections:    Array[TableSection],
    aggregator:  BiFunction[Nullable[Integer], Nullable[Integer], Nullable[Integer]],
    skipColumns: Array[Int]
  ): Int = {
    val columns = Array[Nullable[Integer]](Nullable(null))

    forAllSectionsRows(
      0,
      Integer.MAX_VALUE,
      sections,
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int = {
          val iMax  = row.cells.size()
          var count = 0
          var i     = 0
          while (i < iMax) {
            if (!skipColumns.contains(i)) count += row.cells.get(i).columnSpan
            i += 1
          }
          if (count != 0) {
            columns(0) = aggregator.apply(columns(0), Nullable(Integer.valueOf(count)))
          }
          0
        }
      }
    )

    if (columns(0).isEmpty) 0 else columns(0).get.intValue()
  }

  private def aggregateTotalColumnsWithoutRows(
    sections:   Array[TableSection],
    aggregator: BiFunction[Nullable[Integer], Nullable[Integer], Nullable[Integer]],
    skipRows:   Array[Int]
  ): Int = {
    val columns = Array[Nullable[Integer]](Nullable(null))

    forAllSectionsRows(
      0,
      Integer.MAX_VALUE,
      sections,
      new TableRowManipulator {
        override def apply(row: TableRow, allRowsIndex: Int, sectionRows: ArrayList[TableRow], sectionRowIndex: Int): Int = {
          if (!skipRows.contains(allRowsIndex)) {
            val totalColumns = row.getTotalColumns
            if (totalColumns > 0) { columns(0) = aggregator.apply(columns(0), Nullable(Integer.valueOf(totalColumns))) }
          }
          0
        }
      }
    )

    if (columns(0).isEmpty) 0 else columns(0).get.intValue()
  }

  private def forAllSectionsRows(
    startIndex:  Int,
    count:       Int,
    sections:    Array[TableSection],
    manipulator: TableRowManipulator
  ): Unit =
    if (count <= 0) {
      () // trivial guard clause: nothing to do
    } else {
      var remaining    = count
      var sectionIndex = startIndex
      var allRowsIndex = startIndex

      boundary {
        for (section <- sections) {
          var currentIndex = 0

          if (sectionIndex >= section.rows.size()) {
            sectionIndex -= section.rows.size()
          } else {
            currentIndex = sectionIndex
            sectionIndex = 0

            while (currentIndex < section.rows.size()) {
              val result = manipulator.apply(section.rows.get(currentIndex), allRowsIndex, section.rows, currentIndex)
              if (result == TableRowManipulator.BREAK) break()
              if (result < 0) {
                allRowsIndex -= result // adjust for deleted rows
                remaining += result
              } else {
                currentIndex += result + 1
                remaining -= 1
              }
              if (remaining <= 0) break()
              allRowsIndex += 1
            }
          }
        }
      }
    } // end else

  override def toString: String =
    // NOTE: show not simple name but name of container class if any
    this.getClass.getName.substring(getClass.getPackage.getName.length + 1) + "{" +
      "header=" + header +
      ",\nseparator=" + separator +
      ",\nbody=" + body +
      ",\ncaption=" + caption +
      ",\noptions=" + options +
      ",\ntrackedOffsets=" + trackedOffsets +
      "}"
}

